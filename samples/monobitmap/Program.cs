/*
Monochrome Bitmap - UxROM mapper with CHR RAM
Based on 8bitworkshop monobitmap.c

Demonstrates UxROM mapper (mapper 2) with CHR RAM for
monochrome bitmap display. Instead of loading tile graphics
from a CHR ROM file (.s), tile patterns are written to
CHR RAM at runtime via VRAM writes.

Key concepts:
- UxROM mapper: NES_MAPPER 2 in the iNES header
- CHR RAM: NES_CHR_BANKS 0 (no CHR ROM, pattern RAM is writable)
- Tile patterns uploaded to pattern table via vram_write()
- Direct PPU register manipulation via poke()
*/

// Monochrome palette (from 8bitworkshop monobitmap.c)
byte[] PALETTE = new byte[] {
    DarkViolet,
    White, DarkViolet, White, 0x00,
    DarkViolet, White, White, 0x00,
    White, DarkViolet, White, 0x00,
    DarkViolet, White, White
};

// Tile patterns for CHR RAM (16 bytes per tile: 8 plane 0, 8 plane 1)
// For monochrome display, only plane 0 is used (set bit = white pixel)
byte[] TILES = new byte[] {
    // Tile 0: blank
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 1: filled block (all pixels white)
    0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 2: horizontal line (top scanline)
    0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 3: horizontal line (bottom scanline)
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xFF,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 4: vertical line (left edge)
    0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 5: vertical line (right edge)
    0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 6: corner top-left
    0xFF, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 7: corner top-right
    0xFF, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 8: corner bottom-left
    0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0xFF,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 9: corner bottom-right
    0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0xFF,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 10: diagonal line (top-left to bottom-right)
    0x80, 0x40, 0x20, 0x10, 0x08, 0x04, 0x02, 0x01,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 11: X cross pattern
    0x81, 0x42, 0x24, 0x18, 0x18, 0x24, 0x42, 0x81,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // Tile 12: checkerboard pattern
    0xAA, 0x55, 0xAA, 0x55, 0xAA, 0x55, 0xAA, 0x55,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
};

// Rendering is already off at startup (PPU_MASK=0) and NMI is enabled.
// Do NOT poke(PPU_CTRL, 0) here — that disables NMI, causing ppu_wait_nmi to hang.

// Upload tile patterns to CHR RAM at $0000 (pattern table 0)
vram_adr(0x0000);
vram_write(TILES);

// Clear nametable A
vram_adr(0x2000);
vram_fill(0x00, 0x0400);

// Setup attribute table (all palette 0)
vram_adr(0x23C0);
vram_fill(0x00, 0x40);

// Draw a rectangle border with diagonal fill
// Top edge: row 4, columns 4-27
vram_adr(NTADR_A(4, 4));
vram_put(6);
for (byte i = 0; i < 22; i++)
    vram_put(2);
vram_put(7);

// Middle rows: borders + diagonal interior (rows 5-24)
// Sequential writes: 24 tiles + 8 blanks = 32 per row
vram_adr(NTADR_A(4, 5));
for (byte row = 0; row < 20; row++)
{
    vram_put(4);
    for (byte col = 0; col < 22; col++)
        vram_put(10);
    vram_put(5);
    for (byte s = 0; s < 8; s++)
        vram_put(0);
}

// Bottom edge: row 25
vram_put(8);
for (byte i = 0; i < 22; i++)
    vram_put(3);
vram_put(9);

// Select background CHR bank 0
bank_bg(0);

// Set palette
pal_bg(PALETTE);

// Reset scroll via poke (PPU register manipulation)
// Read PPU_STATUS to reset the address/scroll latch toggle
peek(PPU_STATUS);
poke(PPU_SCROLL, 0);
poke(PPU_SCROLL, 0);

// Enable rendering
ppu_on_all();

while (true)
{
    ppu_wait_nmi();
}
