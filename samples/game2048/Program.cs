// ===== GAME STATE =====

byte[] map = new byte[16];
byte[] prev_map = new byte[16];
byte[] tile_row = new byte[4];

byte i = 0, j = 0, k = 0, sprite = 0;
byte keys = 0, keys_changed = 0, has_changed = 0, game_on = 0;
byte oam_id = 0, notify = 0, count = 0, counter = 0, state = 0;
byte p0 = 0, p1 = 0, p2 = 0, p3 = 0;
byte v0 = 0, v1 = 0, v2 = 0, v3 = 0;
byte sx = 0, sy = 0, rseed = 0;
ushort addr = 0;

// ===== INIT GRAPHICS =====

pal_all(PALETTE);
vram_adr(NAMETABLE_A);
vram_write(BKG0);
vram_adr(NAMETABLE_C);
vram_write(BKG1);
oam_clear();
ppu_on_all();

// ===== TITLE SCREEN =====

rseed = 0;
while (true)
{
    ppu_wait_nmi();
    rseed = (byte)(rseed + 1);
    if ((pad_poll(0) & PAD.START) != 0) break;
}
set_rand(rseed);
scroll(0, 240);

// ===== OUTER GAME LOOP =====

while (true)
{
    // Reset map
    game_on = 1;
    keys_changed = 0;
    for (i = 0; i < 16; i = (byte)(i + 1))
    {
        map[i] = 0;
        prev_map[i] = 0;
    }
    oam_clear();

    // Add 2 initial cells
    for (state = 0; state < 2; state = (byte)(state + 1))
    {
        count = 0;
        for (k = 0; k < 16; k = (byte)(k + 1))
        {
            if ((map[k] & 0x0F) == 0) count = (byte)(count + 1);
        }
        if (count > 0)
        {
            counter = (byte)(rand8() % count);
            for (k = 0; k < 16; k = (byte)(k + 1))
            {
                if ((map[k] & 0x0F) == 0)
                {
                    if (counter == 0)
                    {
                        if (rand8() <= 230)
                            map[k] = 0xF1;
                        else
                            map[k] = 0xF2;
                        break;
                    }
                    counter = (byte)(counter - 1);
                }
            }
        }
    }

    // Clear message area
    set_vram_update(UPD_BLANK);
    ppu_wait_nmi();
    // Switch to vrambuf mode for the game loop
    vrambuf_clear();
    set_vram_update(updbuf);

    // ===== INNER GAME LOOP =====

    while (game_on == 1)
    {
        // ----- Redraw changed tiles -----
        for (i = 0; i < 4; i = (byte)(i + 1))
        {
            for (j = 0; j < 4; j = (byte)(j + 1))
            {
                p0 = (byte)(i * 4 + j);
                v0 = (byte)(map[p0] & 0x0F);
                v1 = (byte)(prev_map[p0] & 0x0F);
                if (v0 != v1)
                {
                    sprite = (byte)(v0 << 2);
                    if (v0 > 7) sprite = (byte)(sprite + 0xA0);
                    else if (v0 > 3) sprite = (byte)(sprite + 0x70);
                    else sprite = (byte)(sprite + 0x40);

                    // Pre-compute NTADR args to avoid TEMP clobber in multiply
                    p0 = (byte)(4 + j * 6);
                    p1 = (byte)(4 + i * 6);
                    addr = NTADR_C(p0, p1);
                    for (k = 0; k < 4; k = (byte)(k + 1))
                    {
                        // Pre-compute into locals to avoid stelem add expression bug
                        v2 = sprite;
                        v3 = (byte)(sprite + 1);
                        p2 = (byte)(sprite + 2);
                        p3 = (byte)(sprite + 3);
                        tile_row[0] = v2;
                        tile_row[1] = v3;
                        tile_row[2] = p2;
                        tile_row[3] = p3;
                        vrambuf_put(addr, tile_row, 4);
                        sprite = (byte)(sprite + 0x10);
                        addr = (ushort)(addr + 32);
                    }
                    ppu_wait_nmi();
                    vrambuf_clear();
                }
            }
        }

        // ----- Show notification sprites -----
        oam_id = 0;
        sy = 24;
        for (i = 0; i < 4; i = (byte)(i + 1))
        {
            sx = 32;
            for (j = 0; j < 4; j = (byte)(j + 1))
            {
                p0 = (byte)(i * 4 + j);
                notify = (byte)((map[p0] >> 4) & 0x0F);
                if (notify > 0)
                {
                    oam_id = oam_spr((byte)(sx - 4), sy, 0x1A, 0, oam_id);
                    oam_id = oam_spr((byte)(sx + 4), sy, 0x1B, 0, oam_id);
                    notify = (byte)(notify - 1);
                    map[p0] = (byte)((map[p0] & 0x0F) | (byte)(notify << 4));
                }
                else
                {
                    oam_id = oam_spr(0, 0, 0, 0, oam_id);
                    oam_id = oam_spr(0, 0, 0, 0, oam_id);
                }
                sx = (byte)(sx + 48);
            }
            sy = (byte)(sy + 48);
        }
        oam_hide_rest(oam_id);
        ppu_wait_nmi();

        // ----- Check state -----
        state = 0;
        count = 0;
        for (k = 0; k < 16; k = (byte)(k + 1))
        {
            if ((map[k] & 0x0F) == 0) count = (byte)(count + 1);
        }
        if (count == 0)
        {
            state = 1;
            for (k = 0; k < 16; k = (byte)(k + 1))
            {
                if ((map[k] & 0x0F) == 11) state = 2;
            }
            if (state == 1)
            {
                // Check horizontal adjacency
                for (i = 0; i < 4; i = (byte)(i + 1))
                {
                    for (j = 0; j < 3; j = (byte)(j + 1))
                    {
                        p0 = (byte)(i * 4 + j);
                        p1 = (byte)(p0 + 1);
                        v0 = (byte)(map[p0] & 0x0F);
                        v1 = (byte)(map[p1] & 0x0F);
                        if (v0 == v1) state = 0;
                    }
                }
                // Check vertical adjacency
                for (i = 0; i < 3; i = (byte)(i + 1))
                {
                    for (j = 0; j < 4; j = (byte)(j + 1))
                    {
                        p0 = (byte)(i * 4 + j);
                        p1 = (byte)(p0 + 4);
                        v0 = (byte)(map[p0] & 0x0F);
                        v1 = (byte)(map[p1] & 0x0F);
                        if (v0 == v1) state = 0;
                    }
                }
            }
        }

        // ----- Handle game state -----
        if (state == 0)
        {
            // Game continues - handle input
            keys = (byte)pad_poll(0);
            if (keys != keys_changed)
            {
                has_changed = 0;

                // Copy map to prev_map before any move
                if ((keys_changed & (byte)PAD.LEFT) != 0 ||
                    (keys_changed & (byte)PAD.RIGHT) != 0 ||
                    (keys_changed & (byte)PAD.UP) != 0 ||
                    (keys_changed & (byte)PAD.DOWN) != 0)
                {
                    for (k = 0; k < 16; k = (byte)(k + 1))
                    {
                        prev_map[k] = map[k];
                    }
                }

                if ((keys_changed & (byte)PAD.LEFT) != 0)
                {
                    // ===== MOVE LEFT =====
                    for (i = 0; i < 4; i = (byte)(i + 1))
                    {
                        p0 = (byte)(i * 4);
                        p1 = (byte)(i * 4 + 1);
                        p2 = (byte)(i * 4 + 2);
                        p3 = (byte)(i * 4 + 3);

                        // Shift toward p0
                        v0 = (byte)(map[p0] & 0x0F);
                        v1 = (byte)(map[p1] & 0x0F);
                        v2 = (byte)(map[p2] & 0x0F);
                        v3 = (byte)(map[p3] & 0x0F);
                        while (v0 == 0 && (v1 != 0 || v2 != 0 || v3 != 0))
                        {
                            map[p0] = map[p1]; map[p1] = map[p2]; map[p2] = map[p3]; map[p3] = 0;
                            has_changed = 1;
                            v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                            v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        }
                        v1 = (byte)(map[p1] & 0x0F);
                        v2 = (byte)(map[p2] & 0x0F);
                        v3 = (byte)(map[p3] & 0x0F);
                        while (v1 == 0 && (v2 != 0 || v3 != 0))
                        {
                            map[p1] = map[p2]; map[p2] = map[p3]; map[p3] = 0;
                            has_changed = 1;
                            v1 = (byte)(map[p1] & 0x0F); v2 = (byte)(map[p2] & 0x0F);
                            v3 = (byte)(map[p3] & 0x0F);
                        }
                        v2 = (byte)(map[p2] & 0x0F);
                        v3 = (byte)(map[p3] & 0x0F);
                        if (v2 == 0 && v3 != 0)
                        {
                            map[p2] = map[p3]; map[p3] = 0;
                            has_changed = 1;
                        }

                        // Merge toward p0
                        v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                        v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        if (v0 != 0 && v1 == v0)
                        {
                            map[p0] = (byte)((v0 + 1) | 0xF0);
                            map[p1] = map[p2]; map[p2] = map[p3]; map[p3] = 0;
                            has_changed = 1;
                        }
                        else if (v1 != 0 && v2 == v1)
                        {
                            map[p1] = (byte)((v1 + 1) | 0xF0);
                            map[p2] = map[p3]; map[p3] = 0;
                            has_changed = 1;
                        }
                        else if (v2 != 0 && v3 == v2)
                        {
                            map[p2] = (byte)((v2 + 1) | 0xF0);
                            map[p3] = 0;
                            has_changed = 1;
                        }
                    }
                }
                else if ((keys_changed & (byte)PAD.RIGHT) != 0)
                {
                    // ===== MOVE RIGHT =====
                    for (i = 0; i < 4; i = (byte)(i + 1))
                    {
                        p0 = (byte)(i * 4);
                        p1 = (byte)(i * 4 + 1);
                        p2 = (byte)(i * 4 + 2);
                        p3 = (byte)(i * 4 + 3);

                        // Shift toward p3
                        v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                        v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        while (v3 == 0 && (v2 != 0 || v1 != 0 || v0 != 0))
                        {
                            map[p3] = map[p2]; map[p2] = map[p1]; map[p1] = map[p0]; map[p0] = 0;
                            has_changed = 1;
                            v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                            v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        }
                        v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                        v2 = (byte)(map[p2] & 0x0F);
                        while (v2 == 0 && (v1 != 0 || v0 != 0))
                        {
                            map[p2] = map[p1]; map[p1] = map[p0]; map[p0] = 0;
                            has_changed = 1;
                            v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                            v2 = (byte)(map[p2] & 0x0F);
                        }
                        v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                        if (v1 == 0 && v0 != 0)
                        {
                            map[p1] = map[p0]; map[p0] = 0;
                            has_changed = 1;
                        }

                        // Merge toward p3
                        v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                        v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        if (v3 != 0 && v2 == v3)
                        {
                            map[p3] = (byte)((v3 + 1) | 0xF0);
                            map[p2] = map[p1]; map[p1] = map[p0]; map[p0] = 0;
                            has_changed = 1;
                        }
                        else if (v2 != 0 && v1 == v2)
                        {
                            map[p2] = (byte)((v2 + 1) | 0xF0);
                            map[p1] = map[p0]; map[p0] = 0;
                            has_changed = 1;
                        }
                        else if (v1 != 0 && v0 == v1)
                        {
                            map[p1] = (byte)((v1 + 1) | 0xF0);
                            map[p0] = 0;
                            has_changed = 1;
                        }
                    }
                }
                else if ((keys_changed & (byte)PAD.UP) != 0)
                {
                    // ===== MOVE UP =====
                    for (j = 0; j < 4; j = (byte)(j + 1))
                    {
                        p0 = j;
                        p1 = (byte)(j + 4);
                        p2 = (byte)(j + 8);
                        p3 = (byte)(j + 12);

                        // Shift toward p0
                        v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                        v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        while (v0 == 0 && (v1 != 0 || v2 != 0 || v3 != 0))
                        {
                            map[p0] = map[p1]; map[p1] = map[p2]; map[p2] = map[p3]; map[p3] = 0;
                            has_changed = 1;
                            v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                            v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        }
                        v1 = (byte)(map[p1] & 0x0F); v2 = (byte)(map[p2] & 0x0F);
                        v3 = (byte)(map[p3] & 0x0F);
                        while (v1 == 0 && (v2 != 0 || v3 != 0))
                        {
                            map[p1] = map[p2]; map[p2] = map[p3]; map[p3] = 0;
                            has_changed = 1;
                            v1 = (byte)(map[p1] & 0x0F); v2 = (byte)(map[p2] & 0x0F);
                            v3 = (byte)(map[p3] & 0x0F);
                        }
                        v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        if (v2 == 0 && v3 != 0)
                        {
                            map[p2] = map[p3]; map[p3] = 0;
                            has_changed = 1;
                        }

                        // Merge toward p0
                        v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                        v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        if (v0 != 0 && v1 == v0)
                        {
                            map[p0] = (byte)((v0 + 1) | 0xF0);
                            map[p1] = map[p2]; map[p2] = map[p3]; map[p3] = 0;
                            has_changed = 1;
                        }
                        else if (v1 != 0 && v2 == v1)
                        {
                            map[p1] = (byte)((v1 + 1) | 0xF0);
                            map[p2] = map[p3]; map[p3] = 0;
                            has_changed = 1;
                        }
                        else if (v2 != 0 && v3 == v2)
                        {
                            map[p2] = (byte)((v2 + 1) | 0xF0);
                            map[p3] = 0;
                            has_changed = 1;
                        }
                    }
                }
                else if ((keys_changed & (byte)PAD.DOWN) != 0)
                {
                    // ===== MOVE DOWN =====
                    for (j = 0; j < 4; j = (byte)(j + 1))
                    {
                        p0 = j;
                        p1 = (byte)(j + 4);
                        p2 = (byte)(j + 8);
                        p3 = (byte)(j + 12);

                        // Shift toward p3
                        v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                        v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        while (v3 == 0 && (v2 != 0 || v1 != 0 || v0 != 0))
                        {
                            map[p3] = map[p2]; map[p2] = map[p1]; map[p1] = map[p0]; map[p0] = 0;
                            has_changed = 1;
                            v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                            v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        }
                        v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                        v2 = (byte)(map[p2] & 0x0F);
                        while (v2 == 0 && (v1 != 0 || v0 != 0))
                        {
                            map[p2] = map[p1]; map[p1] = map[p0]; map[p0] = 0;
                            has_changed = 1;
                            v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                            v2 = (byte)(map[p2] & 0x0F);
                        }
                        v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                        if (v1 == 0 && v0 != 0)
                        {
                            map[p1] = map[p0]; map[p0] = 0;
                            has_changed = 1;
                        }

                        // Merge toward p3
                        v0 = (byte)(map[p0] & 0x0F); v1 = (byte)(map[p1] & 0x0F);
                        v2 = (byte)(map[p2] & 0x0F); v3 = (byte)(map[p3] & 0x0F);
                        if (v3 != 0 && v2 == v3)
                        {
                            map[p3] = (byte)((v3 + 1) | 0xF0);
                            map[p2] = map[p1]; map[p1] = map[p0]; map[p0] = 0;
                            has_changed = 1;
                        }
                        else if (v2 != 0 && v1 == v2)
                        {
                            map[p2] = (byte)((v2 + 1) | 0xF0);
                            map[p1] = map[p0]; map[p0] = 0;
                            has_changed = 1;
                        }
                        else if (v1 != 0 && v0 == v1)
                        {
                            map[p1] = (byte)((v1 + 1) | 0xF0);
                            map[p0] = 0;
                            has_changed = 1;
                        }
                    }
                }

                // Add new cell after successful move
                if (has_changed != 0)
                {
                    count = 0;
                    for (k = 0; k < 16; k = (byte)(k + 1))
                    {
                        if ((map[k] & 0x0F) == 0) count = (byte)(count + 1);
                    }
                    if (count > 0)
                    {
                        counter = (byte)(rand8() % count);
                        for (k = 0; k < 16; k = (byte)(k + 1))
                        {
                            if ((map[k] & 0x0F) == 0)
                            {
                                if (counter == 0)
                                {
                                    if (rand8() <= 230)
                                        map[k] = 0xF1;
                                    else
                                        map[k] = 0xF2;
                                    break;
                                }
                                counter = (byte)(counter - 1);
                            }
                        }
                    }
                }

                keys_changed = keys;
            }
        }
        else if (state == 1)
        {
            // GAME LOST
            set_vram_update(UPD_LOST);
            ppu_wait_nmi();
            set_vram_update(0);
            while (true)
            {
                ppu_wait_nmi();
                if ((pad_poll(0) & PAD.START) != 0) break;
            }
            game_on = 0;
        }
        else
        {
            // GAME WON
            set_vram_update(UPD_WON);
            ppu_wait_nmi();
            set_vram_update(0);
            while (true)
            {
                ppu_wait_nmi();
                if ((pad_poll(0) & PAD.START) != 0) break;
            }
            game_on = 0;
        }
    }
