static void fade_in()
{
    for (byte vb = 0; vb <= 4; vb = (byte)(vb + 1))
    {
        pal_bright(vb);
        ppu_wait_frame();
        ppu_wait_frame();
        ppu_wait_frame();
        ppu_wait_frame();
    }
}
