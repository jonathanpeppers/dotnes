#define NOTE_TABLE note_table_49
#define BASS_NOTE 36

// --- Music player ---

byte cur_duration = 0;

const byte music1[]; // forward declaration -- data at end of file
const byte* music_ptr = music1;

void play_music() {
  static byte chs = 0;
  if (music_ptr) {
    while (cur_duration == 0) {
      byte note = *music_ptr++;
      if ((note & 0x80) == 0) {
        if (note >= BASS_NOTE || (chs & 4)) {
          int period = NOTE_TABLE[note & 63];
          if (!(chs & 1)) {
            // APU_PULSE_DECAY(0, period, DUTY_25, 2, 10)
            *(byte*)0x4002 = period & 0xff;
            *(byte*)0x4003 = ((period) >> 8) | (10 << 3);
            *(byte*)0x4000 = 0x42;
            chs |= 1;
          } else if (!(chs & 2)) {
            // APU_PULSE_DECAY(1, period, DUTY_25, 2, 10)
            *(byte*)0x4006 = period & 0xff;
            *(byte*)0x4007 = ((period) >> 8) | (10 << 3);
            *(byte*)0x4004 = 0x42;
            chs |= 2;
          }
        } else {
          int period = note_table_tri[note & 63];
          // APU_TRIANGLE_LENGTH(period, 15)
          *(byte*)0x4008 = 0x7f;
          *(byte*)0x400A = period & 0xff;
          *(byte*)0x400B = ((period) >> 8) | (15 << 3);
          chs |= 4;
        }
      } else {
        if (note == 0xff)
          music_ptr = NULL;
        cur_duration = note & 63;
        chs = 0;
      }
    }
    cur_duration--;
  }
}

void start_music(const byte* music) {
  music_ptr = music;
  cur_duration = 0;
}

void main(void)
{
  // apu_init() inlined
  memcpy((void*)0x4000, "\x30\x08\x00\x00\x30\x08\x00\x00\x80\x00\x00\x00\x30\x00\x00\x00\x00\x00\x00", 19);
  *(byte*)0x4017 = 0x40;
  *(byte*)0x4015 = 0x0F;

  start_music(music1);

  while (1) {
    ppu_wait_nmi();
    play_music();
  }
}

